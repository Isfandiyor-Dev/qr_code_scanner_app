class Contact {
  String firstName;
  String? lastName;
  String? companyName;
  String? job;
  String phone;
  String? email;
  String? website;
  String? city;
  String? country;

  Contact({
    required this.firstName,
    required this.lastName,
    required this.companyName,
    required this.job,
    required this.phone,
    required this.email,
    required this.website,
    required this.city,
    required this.country,
  });
}
